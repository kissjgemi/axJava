    public void showText(String actualText) {
        txtArea.setForeground(Color.BLUE);
        txtArea.setText(actualText);

    }

    @Override
    protected void paintComponent(Graphics g) {
        super.paintComponent(g);
        switch (state) {
            case PROLOG: {
                g.drawImage(GRAPHITY_PROLOG, 0, 0,
                        this.getWidth(), this.getHeight(), this);
                String str = PROCESS_STATE.PROLOG.toString();
                g.setColor(Color.WHITE);
                g.setFont(new Font("Serif", Font.BOLD, 31));
                int width = g.getFontMetrics().stringWidth(str);
                g.drawString(str,
                        (GRAPHITY_WIDTH - width) / 2,
                        TEXT_MARGIN_V / 2);
                break;
            }
            case EXIT: {
                g.drawImage(GRAPHITY_FINALE, 0, 0,
                        this.getWidth(), this.getHeight(), this);
                String str = PROCESS_STATE.EPILOG.toString();
                g.setColor(Color.WHITE);
                g.setFont(new Font("Serif", Font.BOLD, 31));
                int width = g.getFontMetrics().stringWidth(str);
                g.drawString(str,
                        (GRAPHITY_WIDTH - width) / 2,
                        GRAPHITY_HEIGHT / 6);
                break;
            }
            default: {
                g.drawImage(GRAPHITY_MAIN, 0, 0,
                        this.getWidth(), this.getHeight(), this);
            }
        }
        if (c != null) {
            c.drawGraphity(g);
        }
    }

