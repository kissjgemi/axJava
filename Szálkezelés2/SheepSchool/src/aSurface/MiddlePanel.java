    public void setup() {
        System.out.println("middlePanel: "
                + this.getWidth() + "x" + this.getHeight());

        txtArea.setForeground(Color.WHITE);
        txtArea.setText(TOTAL_TEXT);

        TEXT_WIDTH = (int) txtArea.getPreferredSize().getWidth() + 8;
        TEXT_HEIGHT = (int) txtArea.getPreferredSize().getHeight() + 8;

        TEXT_MARGIN_H = (GRAPHITY_WIDTH - TEXT_WIDTH) / 2;
        TEXT_MARGIN_V = (GRAPHITY_HEIGHT - TEXT_HEIGHT) / 2;

        txtArea.setSize(TEXT_WIDTH, TEXT_HEIGHT);
        scrollPanel.setSize(TEXT_WIDTH, TEXT_HEIGHT);

        scrollPanel.setLocation(TEXT_MARGIN_H, TEXT_MARGIN_V);

        this.revalidate();
        System.out.println(">" + TOTAL_TEXT);
        System.out.println(TEXT_WIDTH + " : " + TEXT_HEIGHT);

        this.revalidate();
    }

